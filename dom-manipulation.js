// DOM (Document Object Model) is a programming interface for web documents.
// It represents the structure of a webpage as a tree of objects, allowing JavaScript
// to interact with and manipulate HTML and CSS dynamically.

// Accessing DOM elements
// Selectors
const heading = document.getElementById('title');
console.log(heading);

const items = document.getElementsByClassName('item');
console.log(items);

const paragraph = document.getElementsByTagName('p');
console.log(paragraph);

const title = document.querySelector('title');
console.log(title);

const listItems = document.querySelectorAll('.item');
console.log(listItems);

// Manipulating content
heading.textContent = 'Document Object Model (DOM)';

const container = document.querySelector('.container');
container.innerHTML = '<p>This is generated by JavaScript.</p>';

const link = document.querySelector('a');
link.setAttribute('href', 'https://en.wikipedia.org/wiki/Document_Object_Model');
link.textContent = 'Learn more.';

const body = document.getElementsByTagName('body');
console.log(body);

// Manipulating styles
heading.style.color = 'blue';
heading.style.fontSize = '2.5rem';
link.classList.add('active');
link.classList.remove('inactive');
link.classList.toggle('highlight');

// Create elements
const button = document.createElement('button');
button.textContent = 'Click Me';
container.appendChild(button);

const list = document.querySelector('#list');
const newItem = document.createElement('li');
newItem.textContent = 'This is generated by JavaScript';
newItem.classList.add('generated');
list.insertBefore(newItem, list.firstChild);

// Remove elements
// list.removeChild(newItem);
newItem.remove();

// Event handling
button.addEventListener('click', () => {
    alert('Button clicked');
});

// Traversing the DOM
console.log(list.parentNode);
console.log(list.childNodes);
console.log(list.nextSibling);
console.log(list.previousSibling);
console.log(list.firstChild);
console.log(list.firstElementChild);
console.log(list.lastElementChild);