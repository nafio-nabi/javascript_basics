// Operators are symbols that allow you to perform operations on values (variables or data).
// JavaScript different types of operators: arithmetic, assignment, comparison, and logical.

// Arithmetic operators:

// Addition. 
console.log(2 + 2);

// Subtraction.
console.log(5 - 2);

// Multiplication.
console.log(5 * 2);

// Division.
console.log(10 / 2);

// Modulus
console.log(7 % 3);
console.log(6 % 2);

// Exponentiation
console.log(2 ** 2);

// Assignment operators:

// Assignment operator.
let x = 10;
console.log(x);

// Add and assign operator.
x += 2;
console.log(x);

// Subtract and assign operator.
x -= 1;
console.log(x);

// Multiply and assign operator.
x *= 2;
console.log(x);

// Multiply and assign operator.
x /= 2;
console.log(x);

// Comparison operators:

// Equal to (loose equality)
console.log(2 == 2);

// Equal to (loose equality)
console.log(2 == "2");

// Equal to (strict equality)
console.log(2 === "2");

// Equal to (strict equality)
console.log(2 === 2);

// Not equal to (loose)
console.log(2 != 2);

// Not equal to (loose)
console.log(2 != "2");

// Not equal to (strict)
console.log(2 !== 2);

// Not equal to (strict)
console.log(2 !== "2");

// Greater than
console.log(5 > 10);

// Greater than or equal to
console.log(5 >= 5);
console.log(5 >= 10);

// Less than
console.log(5 < 10);

// Less than or equal to
console.log(5 <= 5);
console.log(5 <= 10);

// Logical operators:

// AND
console.log(true && true);
console.log(true && false);

// OR
console.log(true || true);
console.log(false || true);

// NOT
console.log(!true);
console.log(!false);

// String concatenation
console.log("My name is" + " " + "John.")